def _mock_predict_pil(_: Image.Image) -> Tuple[Dict[str, float], str]:
    # Mock consistente (ajústalo si quieres)
    probs = {"glioma": 0.10, "meningioma": 0.08, "pituitary": 0.05, "no_tumor": 0.77}
