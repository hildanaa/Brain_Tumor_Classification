def _mock_gradcam_overlay(pil_img: Image.Image) -> Tuple[Image.Image, np.ndarray]:
    # Heatmap circular fake + overlay simple para UI
    img = pil_img.convert("RGB")
    w, h = img.size
    yy, xx = np.mgrid[0:h, 0:w]
    cx, cy = int(w * 0.55), int(h * 0.52)
    dist = np.sqrt((xx - cx) ** 2 + (yy - cy) ** 2)
    heatmap = np.exp(-(dist ** 2) / (2 * (0.18 * min(w, h)) ** 2))
    heatmap = (heatmap - heatmap.min()) / (heatmap.max() - heatmap.min() + 1e-8)

    img_np = np.array(img).astype(np.float32)
    hm_rgb = np.stack([heatmap, np.zeros_like(heatmap), 1 - heatmap], axis=-1) * 255.0
    overlay_np = (0.65 * img_np + 0.35 * hm_rgb).clip(0, 255).astype(np.uint8)
    return Image.fromarray(overlay_np), heatmap.astype(np.float32)

def _basic_heatmap_features(heatmap: np.ndarray) -> Dict[str, Any]:
    # Features mínimos para el agente (si no hay extractor todavía)
    hm = np.array(heatmap, dtype=np.float32)
    if hm.ndim == 3:
        hm = hm[..., 0]
    hm = np.nan_to_num(hm, nan=0.0, posinf=0.0, neginf=0.0)

    # Normaliza si viene raro
    if hm.max() > 1.0:
        hm = hm / (hm.max() + 1e-8)

    thr = 0.6
    focus_ratio = float((hm > thr).mean())
    peak = float(hm.max())

    h, w = hm.shape
    y, x = np.mgrid[0:h, 0:w]
    total = float(hm.sum() + 1e-8)
    com_x = float((x * hm).sum() / total)
    com_y = float((y * hm).sum() / total)

    # num_regions: lo dejamos como None si no hay skimage/scipy
    return {
        "focus_ratio": focus_ratio,
        "num_regions": None,
        "center_of_mass": (com_x, com_y),
        "peak_intensity": peak,
    }

def _mock_explain(pred_label: str, probs: Dict[str, float], heatmap_features: Dict[str, Any]) -> str:
    items = sorted(probs.items(), key=lambda x: x[1], reverse=True)
    top1_label, top1 = items[0]
    top2 = items[1][1] if len(items) > 1 else 0.0
    margin = top1 - top2

    if top1 >= 0.80 and margin >= 0.20:
        conf = "High"
    elif top1 >= 0.60:
        conf = "Medium"
    else:
        conf = "Low"

    focus_ratio = float(heatmap_features.get("focus_ratio", 0.0))
    pattern = "localized" if focus_ratio < 0.18 else "diffuse"

    return (
        f"The model predicts **{pred_label}** with **{top1:.1%}** confidence ({conf}).\n\n"
        f"Attention appears **{pattern}** (focus_ratio={focus_ratio:.2f}), suggesting the prediction relied on "
        f"{'focal regions' if pattern=='localized' else 'broader/global patterns'}.\n\n"
        f"If confidence is low, interpret cautiously.\n\n"
        f"**Disclaimer:** Educational/research use only. Not for medical diagnosis."
    )


